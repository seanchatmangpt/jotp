    private static String formatTimestamp(Instant instant) {
        LocalDateTime ldt = LocalDateTime.ofInstant(instant, ZoneId.systemDefault());
        return ldt.format(DateTimeFormatter.ofPattern("yyyy-MM-dd HH:mm:ss"));
    }

    private static String escapeHtml(String text) {
        return text.replace("&", "&amp;")
                .replace("<", "&lt;")
                .replace(">", "&gt;")
                .replace("\"", "&quot;")
                .replace("'", "&#x27;");
    }

    @SuppressWarnings("unchecked")
    private static String toJsonString(Object obj) {
        if (obj instanceof Map<?, ?> map) {
            StringBuilder json = new StringBuilder("{");
            boolean first = true;
            for (Map.Entry<?, ?> entry : map.entrySet()) {
                if (!first) json.append(",");
                first = false;
                json.append("\"").append(entry.getKey()).append("\":");
                json.append(toJsonString(entry.getValue()));
            }
            json.append("}");
            return json.toString();
        } else if (obj instanceof List<?> list) {
            StringBuilder json = new StringBuilder("[");
            for (int i = 0; i < list.size(); i++) {
                if (i > 0) json.append(",");
                json.append(toJsonString(list.get(i)));
            }
            json.append("]");
            return json.toString();
        } else if (obj instanceof String str) {
            return "\"" + str.replace("\\", "\\\\").replace("\"", "\\\"") + "\"";
        } else if (obj instanceof Number || obj instanceof Boolean) {
            return String.valueOf(obj);
        } else {
            return "\"" + String.valueOf(obj) + "\"";
        }
    }

    /** Represents a single benchmark result. */
    public record BenchmarkResult(
            String name,
            String mode,
            int threads,
            int forks,
            double score,
            double scoreError,
            double scoreUnit,
            Map<String, Double> percentiles,
            String jvmVersion,
            String vmName) {
        public Map<String, Object> toMap() {
            Map<String, Object> map = new LinkedHashMap<>();
            map.put("name", name);
            map.put("mode", mode);
            map.put("threads", threads);
            map.put("forks", forks);
            map.put("score", score);
            map.put("scoreError", scoreError);
            map.put("scoreUnit", scoreUnit);
            map.put("percentiles", percentiles);
            return map;
        }

        public Optional<Double> getPercentile(String percentile) {
            return Optional.ofNullable(percentiles.get(percentile));
        }
    }

    /** Represents metadata about the benchmark run. */
    public record Metadata(
            String jvmVersion,
            String vmName,
            String osName,
            String osArch,
            int availableProcessors,
            String version) {
        public static Metadata empty() {
            return new Metadata("Unknown", "Unknown", "Unknown", "Unknown", 0, "1.0.0");
        }

        public Map<String, Object> toMap() {
            Map<String, Object> map = new LinkedHashMap<>();
            map.put("jvmVersion", jvmVersion);
            map.put("vmName", vmName);
            map.put("osName", osName);
            map.put("osArch", osArch);
            map.put("availableProcessors", availableProcessors);
            map.put("version", version);
            return map;
        }
    }

    /** Represents trend analysis across multiple benchmark runs. */
    public record TrendAnalysis(Map<String, List<Double>> metricHistory, Instant analyzedAt) {
        public static TrendAnalysis noData() {
            return new TrendAnalysis(Map.of(), Instant.now());
        }

        public Optional<Double> getAverageTrend(String benchmarkName) {
            List<Double> history = metricHistory.get(benchmarkName);
            if (history == null || history.size() < 2) {
                return Optional.empty();
            }

            double first = history.get(0);
            double last = history.get(history.size() - 1);

            if (first == 0) {
                return Optional.empty();
            }

            return Optional.of(((last - first) / first) * 100.0);
        }

        public String getTrendDirection(String benchmarkName) {
            Optional<Double> trend = getAverageTrend(benchmarkName);
            if (trend.isEmpty()) {
                return "No Data";
            }

            double value = trend.orElseThrow();
            if (Math.abs(value) < 2.0) {
                return "Stable";
            } else if (value > 0) {
                return "Improving";
            } else {
                return "Degraded";
            }
        }
    }

    /** Output format options for reports. */
    public enum ReportFormat {
        JSON,
        HTML
    }
