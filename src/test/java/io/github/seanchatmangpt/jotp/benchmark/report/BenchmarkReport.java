    private static Metadata extractMetadata(List<BenchmarkResult> results) {
        if (results.isEmpty()) {
            return Metadata.empty();
        }

        BenchmarkResult first = results.get(0);
        return new Metadata(
                first.jvmVersion(),
                first.vmName(),
                System.getProperty("os.name"),
                System.getProperty("os.arch"),
                Runtime.getRuntime().availableProcessors(),
                "1.0.0" // Version should be injected at build time
                );
    }

    private Map<String, Object> generateSummary() {
        Map<String, Object> summary = new LinkedHashMap<>();

        DoubleSummaryStatistics throughputStats =
                results.stream().mapToDouble(BenchmarkResult::score).summaryStatistics();

        DoubleSummaryStatistics latencyStats =
                results.stream()
                        .mapToDouble(BenchmarkResult::scoreError)
                        .filter(d -> d > 0)
                        .summaryStatistics();

        summary.put("totalBenchmarks", results.size());
        summary.put(
                "throughput",
                Map.of(
                        "average", throughputStats.getAverage(),
                        "min", throughputStats.getMin(),
                        "max", throughputStats.getMax(),
                        "count", throughputStats.getCount()));

        if (latencyStats.getCount() > 0) {
            summary.put(
                    "latency",
                    Map.of(
                            "average", latencyStats.getAverage(),
                            "min", latencyStats.getMin(),
                            "max", latencyStats.getMax()));
        }

        // SLA compliance
        long slaPassCount =
                results.stream()
                        .filter(r -> r.score() >= 10000) // 10K ops/s target
                        .count();

        summary.put(
                "slaCompliance",
                Map.of(
                        "passCount",
                        slaPassCount,
                        "totalCount",
                        results.size(),
                        "passRate",
                        (double) slaPassCount / results.size()));

        return summary;
    }

