        summary.append("            <div class=\"card\">\n");
        summary.append("                <h3>Average Throughput</h3>\n");
        summary.append("                <p class=\"metric\">")
                .append(String.format("%.2f", avgThroughput))
                .append(" ops/s</p>\n");
        summary.append("                <p class=\"status ")
                .append(avgThroughput >= 10000 ? "pass" : "fail")
                .append("\">")
                .append(avgThroughput >= 10000 ? "✓ Meets SLA" : "✗ Below SLA")
                .append("</p>\n");
        summary.append("            </div>\n");

        // Total benchmarks card
        summary.append("            <div class=\"card\">\n");
        summary.append("                <h3>Total Benchmarks</h3>\n");
        summary.append("                <p class=\"metric\">")
                .append(results.size())
                .append("</p>\n");
        summary.append("            </div>\n");

        // JVM info card
