    /**
     * Generates a JSON report with all benchmark results and metadata.
     *
     * @return JSON string representation
     */
    public String generateJsonReport() {
        Map<String, Object> report = new LinkedHashMap<>();
        report.put("generatedAt", generatedAt.toString());
        report.put("metadata", metadata.toMap());
        report.put("results", results.stream().map(BenchmarkResult::toMap).toList());
        report.put("summary", generateSummary());

        return toJsonString(report);
    }

    /**
     * Generates an HTML report with styling and comprehensive metrics.
     *
     * @return HTML string representation
     */
    public String generateHtmlReport() {
        StringBuilder html = new StringBuilder();
        html.append("<!DOCTYPE html>\n");
        html.append("<html lang=\"en\">\n");
        html.append("<head>\n");
        html.append("    <meta charset=\"UTF-8\">\n");
        html.append(
                "    <meta name=\"viewport\" content=\"width=device-width, initial-scale=1.0\">\n");
        html.append("    <title>JOTP Benchmark Report</title>\n");
        html.append("    <style>\n");
        html.append(getCssStyles());
        html.append("    </style>\n");
        html.append("</head>\n");
        html.append("<body>\n");

        // Header
        html.append("    <div class=\"container\">\n");
        html.append("        <h1>JOTP Benchmark Report</h1>\n");
        html.append("        <p class=\"timestamp\">Generated: ")
                .append(formatTimestamp(generatedAt))
                .append("</p>\n");

        // Executive Summary
        html.append(generateExecutiveSummary());

        // Detailed Results
        html.append("        <h2>Detailed Results</h2>\n");
        for (BenchmarkResult result : results) {
            html.append(generateResultCard(result));
        }

        // Footer
        html.append("        <div class=\"footer\">\n");
        html.append("            <p>Generated by JOTP Benchmark Framework v")
                .append(metadata.version())
                .append("</p>\n");
        html.append("        </div>\n");
        html.append("    </div>\n");
        html.append("</body>\n");
        html.append("</html>\n");

        return html.toString();
    }

    /**
     * Calculates trends across multiple benchmark runs.
     *
     * @param previousReports previous benchmark reports for comparison
     * @return TrendAnalysis with metrics and insights
     */
    public TrendAnalysis calculateTrends(List<BenchmarkReport> previousReports) {
        if (previousReports.isEmpty()) {
            return TrendAnalysis.noData();
        }

        Map<String, List<Double>> metricHistory = new HashMap<>();

        // Collect historical data for each benchmark
        for (BenchmarkResult current : results) {
            String benchmarkName = current.name();

            List<Double> throughputs = new ArrayList<>();
            throughputs.add(current.score());

            for (BenchmarkReport previous : previousReports) {
                previous.findResult(benchmarkName).ifPresent(r -> throughputs.add(r.score()));
            }

            metricHistory.put(benchmarkName, throughputs);
        }

        return new TrendAnalysis(metricHistory, generatedAt);
    }

    /**
     * Writes the generated report to a file.
     *
     * @param outputPath path to output file
     * @param format output format (JSON or HTML)
     * @throws IOException if write fails
     */
    public void writeTo(Path outputPath, ReportFormat format) throws IOException {
        String content =
                switch (format) {
                    case JSON -> generateJsonReport();
                    case HTML -> generateHtmlReport();
                };

        Files.writeString(
                outputPath,
                content,
                StandardOpenOption.CREATE,
                StandardOpenOption.TRUNCATE_EXISTING);
    }

    /**
     * Finds a specific benchmark result by name.
     *
     * @param benchmarkName name of the benchmark
     * @return Optional containing the result if found
     */
    public Optional<BenchmarkResult> findResult(String benchmarkName) {
        return results.stream().filter(r -> r.name().equals(benchmarkName)).findFirst();
    }

